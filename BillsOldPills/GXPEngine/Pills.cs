using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using GXPEngine;

public class Pills : Sprite
{
    private bool _isColected = false;
    //------------------------------------------------------------------------------------------------------------
    //                                                  Constructor
    //------------------------------------------------------------------------------------------------------------
    public Pills(int newFrame, int w, int h) : base("PillC.png")
    {
        this.width = w;
        this.height = h;
    }
    //------------------------------------------------------------------------------------------------------------
    //                                                  Update
    //------------------------------------------------------------------------------------------------------------
    void Update()
    {
        if (_isColected)
        {
            LateDestroy();
        }
    }
    //------------------------------------------------------------------------------------------------------------
    //                                                  CollectPill
    //------------------------------------------------------------------------------------------------------------

    public void CollectPill()
    {
        _isColected = true;
    }



}
